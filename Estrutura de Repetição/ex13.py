entrada = int(input("Digite um número: "))

if entrada == 1:
    print("Domingo!")
elif entrada == 2:
    print("Segunda-Feira!")
elif entrada == 3:
    print("Terça-Feira!")
elif entrada == 4:
    print("Quarta-Feira!")
elif entrada == 5:
    print("Quinta-Feira!")
elif entrada == 6:
    print("Sexta-Feira!")
elif entrada == 7:
    print("Sábado!")
else:
    print("Valor inválido!")